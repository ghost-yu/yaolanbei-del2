    function estimate(w, life, scene) {
      let base = 280;
      if (w < 380) base += 95; else if (w <= 780) base += 65; else if (w <= 1500) base += 145; else base += 220;
      base += life * 30;
      const factor = { pv: 1.18, agri: 1.12, research: 1.26, industry: 1.04 };
      return Math.round(base * factor[scene]);
    }

